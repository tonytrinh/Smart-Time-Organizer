					<li> To complement with the lack of integration of PRISM with SyllabusPlus at the moment </li>
				</ul>
				
			<h4>How to use Smart Timetable Organizer</h4>
				<ul>
					<li> At Home Page, select the number of courses you wish to enroll in (if not 3), then click UPDATE. </li>
					<li> Select the courses, then click PROCEED.
					<li> Three best combinations of the courses will appear, but users can select the number
					of combinations they wish to see by selecting the SELECT NUMBER OF COMBINATIONS SHOWN </li>
					<li> You may check the courses listed above the timetable to see the short names of the courses.</li>
